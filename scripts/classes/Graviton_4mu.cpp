//-----------------------------------------------------------------------------
// Compute average over networks. Default is to use all networks
//-----------------------------------------------------------------------------
double Graviton_4mu(double angle_costhetastar,
                    double angle_costheta1,
                    double angle_costheta2,
                    double angle_phi,
                    double angle_phistar1,
                    double Z1mass,
                    double Z2mass,
                    double mass4l,
                    int first=0, int last=nsGraviton_4mu::nnetworks-1)
{
  if ( first < 0 ) first = 0;
  if ( last  < 0 ) last  = nsGraviton_4mu::nnetworks-1;
  if ( last  > nsGraviton_4mu::nnetworks-1 ) last = nsGraviton_4mu::nnetworks-1;
  if ( first > last )
    {
      std::cerr << "** Graviton_4mu ** first-index > last-index"
                << std::endl; exit(3);
    }

  // Compute average over networks
  std::vector<double> in(nsGraviton_4mu::ninputs);
  in[0] = (angle_costhetastar - nsGraviton_4mu::mean[0]) / nsGraviton_4mu::sigma[0];
  in[1] = (angle_costheta1 - nsGraviton_4mu::mean[1]) / nsGraviton_4mu::sigma[1];
  in[2] = (angle_costheta2 - nsGraviton_4mu::mean[2]) / nsGraviton_4mu::sigma[2];
  in[3] = (angle_phi - nsGraviton_4mu::mean[3]) / nsGraviton_4mu::sigma[3];
  in[4] = (angle_phistar1 - nsGraviton_4mu::mean[4]) / nsGraviton_4mu::sigma[4];
  in[5] = (Z1mass - nsGraviton_4mu::mean[5]) / nsGraviton_4mu::sigma[5];
  in[6] = (Z2mass - nsGraviton_4mu::mean[6]) / nsGraviton_4mu::sigma[6];
  in[7] = (mass4l - nsGraviton_4mu::mean[7]) / nsGraviton_4mu::sigma[7];

  int   n = last - first + 1;
  double x = 0.0;
  for(int i=first; i <= last; i++) x += nsGraviton_4mu::netfun(in, nsGraviton_4mu::weight[i]);
  return x / n;
}

double Graviton_4mu(std::vector<double>& in,
                    int first=0,
                    int last=nsGraviton_4mu::nnetworks-1)
{
  return Graviton_4mu(in[0],
                      in[1],
                      in[2],
                      in[3],
                      in[4],
                      in[5],
                      in[6],
                      in[7], first, last);
}

