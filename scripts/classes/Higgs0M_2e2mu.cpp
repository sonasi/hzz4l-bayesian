//-----------------------------------------------------------------------------
// Compute average over networks. Default is to use all networks
//-----------------------------------------------------------------------------
double Higgs0M_2e2mu(double angle_costhetastar,
                     double angle_costheta1,
                     double angle_costheta2,
                     double angle_phi,
                     double angle_phistar1,
                     double Z1mass,
                     double Z2mass,
                     double mass4l,
                     int first=0, int last=nsHiggs0M_2e2mu::nnetworks-1)
{
  if ( first < 0 ) first = 0;
  if ( last  < 0 ) last  = nsHiggs0M_2e2mu::nnetworks-1;
  if ( last  > nsHiggs0M_2e2mu::nnetworks-1 ) last = nsHiggs0M_2e2mu::nnetworks-1;
  if ( first > last )
    {
      std::cerr << "** Higgs0M_2e2mu ** first-index > last-index"
                << std::endl; exit(3);
    }

  // Compute average over networks
  std::vector<double> in(nsHiggs0M_2e2mu::ninputs);
  in[0] = (angle_costhetastar - nsHiggs0M_2e2mu::mean[0]) / nsHiggs0M_2e2mu::sigma[0];
  in[1] = (angle_costheta1 - nsHiggs0M_2e2mu::mean[1]) / nsHiggs0M_2e2mu::sigma[1];
  in[2] = (angle_costheta2 - nsHiggs0M_2e2mu::mean[2]) / nsHiggs0M_2e2mu::sigma[2];
  in[3] = (angle_phi - nsHiggs0M_2e2mu::mean[3]) / nsHiggs0M_2e2mu::sigma[3];
  in[4] = (angle_phistar1 - nsHiggs0M_2e2mu::mean[4]) / nsHiggs0M_2e2mu::sigma[4];
  in[5] = (Z1mass - nsHiggs0M_2e2mu::mean[5]) / nsHiggs0M_2e2mu::sigma[5];
  in[6] = (Z2mass - nsHiggs0M_2e2mu::mean[6]) / nsHiggs0M_2e2mu::sigma[6];
  in[7] = (mass4l - nsHiggs0M_2e2mu::mean[7]) / nsHiggs0M_2e2mu::sigma[7];

  int   n = last - first + 1;
  double x = 0.0;
  for(int i=first; i <= last; i++) x += nsHiggs0M_2e2mu::netfun(in, nsHiggs0M_2e2mu::weight[i]);
  return x / n;
}

