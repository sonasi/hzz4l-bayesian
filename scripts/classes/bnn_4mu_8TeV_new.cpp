//-----------------------------------------------------------------------------
// Compute average over networks. Default is to use all networks
//-----------------------------------------------------------------------------
double bnn_4mu_8TeV_new(double angle_costhetastar,
                        double angle_costheta1,
                        double angle_costheta2,
                        double angle_phi,
                        double angle_phistar1,
                        double Z1mass,
                        double Z2mass,
                        double mass4l,
                        int first=0, int last=nsbnn_4mu_8TeV_new::nnetworks-1)
{
  if ( first < 0 ) first = 0;
  if ( last  < 0 ) last  = nsbnn_4mu_8TeV_new::nnetworks-1;
  if ( last  > nsbnn_4mu_8TeV_new::nnetworks-1 ) last = nsbnn_4mu_8TeV_new::nnetworks-1;
  if ( first > last )
    {
      std::cerr << "** bnn_4mu_8TeV_new ** first-index > last-index"
                << std::endl; exit(3);
    }

  // Compute average over networks
  std::vector<double> in(nsbnn_4mu_8TeV_new::ninputs);
  in[0] = (angle_costhetastar - nsbnn_4mu_8TeV_new::mean[0]) / nsbnn_4mu_8TeV_new::sigma[0];
  in[1] = (angle_costheta1 - nsbnn_4mu_8TeV_new::mean[1]) / nsbnn_4mu_8TeV_new::sigma[1];
  in[2] = (angle_costheta2 - nsbnn_4mu_8TeV_new::mean[2]) / nsbnn_4mu_8TeV_new::sigma[2];
  in[3] = (angle_phi - nsbnn_4mu_8TeV_new::mean[3]) / nsbnn_4mu_8TeV_new::sigma[3];
  in[4] = (angle_phistar1 - nsbnn_4mu_8TeV_new::mean[4]) / nsbnn_4mu_8TeV_new::sigma[4];
  in[5] = (Z1mass - nsbnn_4mu_8TeV_new::mean[5]) / nsbnn_4mu_8TeV_new::sigma[5];
  in[6] = (Z2mass - nsbnn_4mu_8TeV_new::mean[6]) / nsbnn_4mu_8TeV_new::sigma[6];
  in[7] = (mass4l - nsbnn_4mu_8TeV_new::mean[7]) / nsbnn_4mu_8TeV_new::sigma[7];

  int   n = last - first + 1;
  double x = 0.0;
  for(int i=first; i <= last; i++) x += nsbnn_4mu_8TeV_new::netfun(in, nsbnn_4mu_8TeV_new::weight[i]);
  return x / n;
}

double bnn_4mu_8TeV_new(std::vector<double>& in,
                        int first=0,
                        int last=nsbnn_4mu_8TeV_new::nnetworks-1)
{
  return bnn_4mu_8TeV_new(in[0],
                          in[1],
                          in[2],
                          in[3],
                          in[4],
                          in[5],
                          in[6],
                          in[7], first, last);
}

